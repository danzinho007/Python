# Python possui algumas constantes pré-definidas no módulo math. Algumas das mais comuns são:

# math.pi: valor da constante matemática pi (π) que representa a relação entre o perímetro de uma circunferência e seu diâmetro. O valor é aproximadamente 3.141592653589793.

# math.e: valor da constante matemática e que representa a base dos logaritmos naturais. O valor é aproximadamente 2.718281828459045.

# math.inf: valor infinito positivo, que é maior do que qualquer número finito. Pode ser usado para representar resultados que tendem ao infinito.

# math.nan: valor que representa "Not a Number" (não é um número). É retornado quando um cálculo não pode ser realizado ou quando é indefinido.