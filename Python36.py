# Testando dist(x, y)
import math

# Define dois pontos no espaço
p1 = (1, 2)
p2 = (3, 4)

# Calcula a distância entre os dois pontos
v14 = math.dist(p1, p2)

print('A distância entre {} e {} é igual a {}' .format(p1, p2, v14))

