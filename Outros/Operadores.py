# Operadores

# Aritméticos :
# São usados para realizar operações matemáticas como adição (+), subtração (-), multiplicação (*), divisão 

# Associação
# São usados para verificar se um valor está presente em uma sequência, como in, not in.

# Atribuição
# São usados para atribuir valores a variáveis, como =, +=, -=, *=, /=, %=, **=.

# Bitwise
# São usados para executar operações de bits em valores inteiros, como &, |, ^, ~, <<, >>.

# Comparação
#  São usados para comparar dois valores e retornar True ou False, como ==, !=, <, >, <=, >=.

# Concatenação
# O operador de concatenação (+) é usado para juntar duas ou mais strings em uma única string.

# Conjunto :
# Union (|)  Retorna um novo conjunto contendo todos os elementos de dois ou mais conjuntos. 
# Intersceção (&) Retorna um novo conjunto contendo apenas os elementos que estão presentes em todos os conjuntos fornecidos.
# Diferença (-) Retorna um novo conjunto contendo todos os elementos que estão em A, mas não em B. 
# Diferença Simétrica (^) Retorna um novo conjunto contendo os elementos que estão presentes em um dos conjuntos, mas não em ambos. 
# Pertinência (in) 
# Verifica se um elemento está presente em um conjunto.
# Não-Pertinência (not in) verifica se um elemento não está presente em um conjunto
# Subconjunto (<=) verifica se um conjunto é um subconjunto de outro
# Superset(>=) verifica se um conjunto é um superset de outro
# Subconjunto Próprio (<) verifica se um conjunto é um subconjunto próprio de outro (ou seja, se o conjunto é um subconjunto, mas não igual ao conjunto).
# Superset Próprio (>) verifica se um conjunto é um superset próprio de outro (ou seja, se o conjunto é um superset, mas não igual ao conjunto)

# Identidade
# São usados para comparar objetos pelo seu endereço de memória, como is, is not.

# Lógicos
# São usados para combinar expressões lógicas e retornar True ou False, como and, or, not.
