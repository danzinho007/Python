# Funções que podem manipular conjuntos

# set() - Cria um novo conjunto vazio ou converte um objeto iterável em um conjunto.

# add() - Adiciona um elemento a um conjunto.

# remove() - Remove um elemento de um conjunto. Se o elemento não existir no conjunto, uma exceção KeyError é levantada.

# discard() - Remove um elemento de um conjunto. Se o elemento não existir no conjunto, não acontece nada.

# pop() - Remove e retorna um elemento aleatório de um conjunto. Se o conjunto estiver vazio, uma exceção KeyError é levantada.

# clear() - Remove todos os elementos de um conjunto.

# copy() - Cria uma cópia superficial de um conjunto.