# Sintaxes

# Sintaxe de atribuição: x = 42, onde um valor é atribuído a uma variável.

# Sintaxe de classe: class, onde as classes são definidas.

# Sintaxe de comentário: #, onde linhas de código são comentadas para explicação ou documentação.

# Sintaxe de conjunto: {1, 2, 3}, onde um conjunto é definido.

# Sintaxe de controle de fluxo: if, else, elif, while, for, break, continue, return, que são usados para controlar o fluxo de execução do programa.

# Sintaxe de dicionário: {'chave': 'valor'}, onde um dicionário é definido.

# Sintaxe de exceção: try, except, finally, que são usados para lidar com exceções e erros.

# Sintaxe de expressão: x + 5, onde uma expressão é usada para realizar uma operação.

# Sintaxe de expressão condicional: x if x > 0 else 0, onde uma expressão condicional é usada para fazer uma escolha

# Sintaxe de expressão lambda: lambda x: x + 1, onde uma expressão lambda é definida.

# Sintaxe de formatação de string: f"...", onde uma string formatada é criada.

# Sintaxe de função: def, onde as funções são definidas.

# Sintaxe de importação: import, from ... import ..., onde módulos são importados para serem usados no programa.

# Sintaxe de lista: [1, 2, 3], onde uma lista é definida.

# Sintaxe de tupla: (1, 2, 3), onde uma tupla é definida.
