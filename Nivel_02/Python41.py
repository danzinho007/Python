# Quiz Python
# Qual é o valor de fruta após a execução do código ?

fruta = "maçã"
fruta = fruta + "s"
fruta = fruta * 3

print(fruta)
# Resultado "maçãsmaçãsmaçãs"
# Letra B

# O valor de fruta após a execução do código será "maçãsmaçãsmaçãs".

# Aqui está a explicação passo a passo:

# Inicialmente, fruta é definido como "maçã".
# Em seguida, a linha fruta = fruta + "s" concatena a string "s" à variável fruta, tornando-a "maçãs".
# Por fim, a linha fruta = fruta * 3 multiplica a string "maçãs" por 3, resultando em "maçãsmaçãsmaçãs".
# Portanto, quando você imprime fruta, verá a saída "maçãsmaçãsmaçãs".
